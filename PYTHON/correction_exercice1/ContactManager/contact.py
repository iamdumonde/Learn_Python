class Contact:
    # Le constructeur de la classe Contact est appelé lors de la création d'une nouvelle instance.
    def __init__(self, nom, prenom, telephone, email):

        # Les lignes suivantes initialisent les attributs de l'instance avec les valeurs fournies.
        self.nom = nom
        self.prenom = prenom
        self.telephone = telephone
        self.email = email
